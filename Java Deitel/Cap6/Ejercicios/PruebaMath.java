public class PruebaMath
{
  public static void main(String[] args)
  {
    System.out.println("Método\tResultado");
    System.out.println("abs\t" + Math.abs(23.7));
    System.out.println("ceil\t" + Math.ceil(9.2));
    System.out.println("cos\t" + Math.cos(0.0));
    System.out.println("exp\t" + Math.exp(1.0) + "\t" +  Math.exp(2.0));
    System.out.println("floor\t" + Math.floor(9.2));
    System.out.println("log\t" + Math.log(Math.E * Math.E));
    System.out.println("max\t" + Math.max(2.3, 12.7));
    System.out.println("min\t" + Math.min(2.3, 12.7));
    System.out.println("pow\t" + Math.pow(2.0, 7.0));
    System.out.println("sin\t" + Math.sin(0.0));
    System.out.println("sqrt\t" + Math.sqrt(900.0));
    System.out.println("tan\t" + Math.tan(0.0));
  }
}